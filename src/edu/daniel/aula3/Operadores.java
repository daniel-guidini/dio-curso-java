package edu.daniel.aula3;

public class Operadores {
    public static void main(String[] args) {
        String nomeComposto = "GUARDA"+" CHUVA";
        System.out.println(nomeComposto);
        int numero = 5;
        numero = - numero;
        System.out.println(numero);
        numero = numero * -1;
        System.out.println(numero);
        // operador de negativo
        numero++;
        System.out.println(numero);
        numero--;
        System.out.println(numero);
        boolean luzCasa = true;
        System.out.println(luzCasa);
        System.out.println(!luzCasa);
        System.out.println(luzCasa);

    }
}
